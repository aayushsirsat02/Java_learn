public class Hello {
    public static void main(String[] args) {
        

        //integer variables :

        int a = 10; // 4 bytes
        long b = 20L; // 8 bytes
        short c = 30; // 2 bytes
        byte d = 40; // 1 byte

        //floating point variables :
        float e = 10.5f; // 4 bytes
        double f = 20.5; // 8 bytes
        //character variable :
        char g = 'A'; // 2 bytes
        //boolean variable :
        boolean h = true; // 1 bit (not precisely defined in terms of bytes)
        //string variable :
        String i = "Hello, World!"; // reference type, not a primitive
        //printing the variables
        System.out.println("Integer a: " + a);
        System.out.println("Long b: " + b);
        System.out.println("Short c: " + c);



    }
}
